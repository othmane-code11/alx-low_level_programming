/**
 * add - addition
 * @a: number
 * @b: number
 * Return: result
 */
int add(int a, int b)
{
      return (a + b);
}
/**
 * sub - sub
 * @a: number
 * @b: number
 * Return: result
 */
int sub(int a, int b)
{
      return (a - b);
}
/**
 * mul - mult
 * @a: number
 * @b: number
 * Return: result
 */
int mul(int a, int b)
{
      return (a * b);
}
/**
 * div - divi
 * @a: number
 * @b: number
 * Return: result
 */
int div(int a, int b)
{
      return (a / b);
}
/**
 * mod - mod
 * @a: number
 * @b: number
 * Return: result
 */
int mod(int a, int b)
{
      return (a % b);
}
